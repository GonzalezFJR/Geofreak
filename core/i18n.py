def get_lang(request) -> str:
    """Read language from cookie, default to 'es'."""
    lang = request.cookies.get("lang", DEFAULT_LANG)
    return lang if lang in SUPPORTED_LANGS else DEFAULT_LANG


def t(key: str, lang: str = DEFAULT_LANG) -> str:
    """Translate *key* for *lang*. Returns key itself if missing."""
    return TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANG]).get(key, key)


def t_js(lang: str = DEFAULT_LANG) -> str:
    """Return a JSON string with all JS-needed translations for *lang*."""
    merged: dict[str, str] = {}
    # Pull keys listed in _JS_KEYS from main TRANSLATIONS dicts
    src = TRANSLATIONS.get(lang, TRANSLATIONS[DEFAULT_LANG])
    fallback = TRANSLATIONS[DEFAULT_LANG]
    for key in _JS_KEYS:
        merged[key] = src.get(key, fallback.get(key, key))
    merged.update(_JS_KEYS_EXTRA.get(lang, _JS_KEYS_EXTRA[DEFAULT_LANG]))
    merged.update(_MAPJS_LABELS.get(lang, _MAPJS_LABELS[DEFAULT_LANG]))
    return json.dumps(merged, ensure_ascii=False)


def game_field(game: dict[str, Any], field: str, lang: str) -> str:
    """Get localised field from a game dict.  e.g. game_field(g, 'name', 'en')
    falls back to the bare field when no localised version exists."""
    if lang != DEFAULT_LANG:
        key = f"{field}_{lang}"
        if key in game and game[key]:
            return game[key]
    return game.get(field, "")
